package Information;

public class Utensil
{
  String name;
  String details;
  
  public Utensil(String name, String details)
  {
    this.name = name;
    this.details = details;
  }
  
  public String getName()
  {
    return this.name;
  }
  
  public String getDetails()
  {
    return this.details;
  }
  
  
}
